package com.example.thres.mobileproject;



public class Soups {
    String soup, imageUrl;

    public Soups(String soup, String imageUrl)
    {
        this.soup = soup;
        this.imageUrl = imageUrl;
    }

    public String getSoup()
    {
        return soup;
    }
    public String getImageUrl()
    {
        return imageUrl;
    }
}



